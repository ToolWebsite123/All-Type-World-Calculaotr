/* ---------------- Guide diagrams ---------------- */

function MiniPlot({
  values,
  label,
  logScale = false,
}: {
  values: number[];
  label: string;
  logScale?: boolean;
}) {
  const w = 320, h = 140, padL = 30, padR = 15, padT = 15, padB = 25;
  const xs = values.map((_, i) => i);
  const ys = logScale ? values.map((v) => Math.log10(Math.max(v, 1))) : values;
  const minY = Math.min(...ys, 0);
  const maxY = Math.max(...ys, 1);
  const spanY = maxY - minY || 1;
  const xToPx = (i: number) => padL + (i / Math.max(xs.length - 1, 1)) * (w - padL - padR);
  const yToPx = (v: number) => h - padB - ((v - minY) / spanY) * (h - padT - padB);
  const path = ys.map((v, i) => `${i === 0 ? "M" : "L"} ${xToPx(i).toFixed(1)} ${yToPx(v).toFixed(1)}`).join(" ");
  return (
    <svg viewBox={`0 0 ${w} ${h}`} className="w-full" role="img" aria-label={label}>
      <line x1={padL} y1={h - padB} x2={w - padR} y2={h - padB} stroke="var(--color-border)" />
      <line x1={padL} y1={padT} x2={padL} y2={h - padB} stroke="var(--color-border)" />
      <path d={path} fill="none" stroke="var(--color-primary)" strokeWidth={2} />
      {values.map((_, i) => (
        <circle key={i} cx={xToPx(i)} cy={yToPx(ys[i])} r={3} fill="var(--color-primary)" />
      ))}
      <text x={w / 2} y={h - 6} textAnchor="middle" fontSize={10} fill="var(--color-muted-foreground)">n →</text>
      {logScale && (
        <text x={w - padR} y={padT + 10} textAnchor="end" fontSize={9} fill="var(--color-muted-foreground)">
          log scale
        </text>
      )}
    </svg>
  );
}

