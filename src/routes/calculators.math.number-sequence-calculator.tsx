interface GeometricResult {
  a1: number;
  r: number;
  n: number;
  terms: number[];
  nth: number;
  sum: number;
  steps: Step[];
}

function computeGeometric(a1: number, r: number, n: number): GeometricResult {
  const terms: number[] = [];
  const listMax = Math.min(n, 20);
  for (let i = 1; i <= listMax; i++) terms.push(a1 * Math.pow(r, i - 1));
  const nth = a1 * Math.pow(r, n - 1);
  const sum =
    Math.abs(r - 1) < 1e-15
      ? a1 * n
      : (a1 * (1 - Math.pow(r, n))) / (1 - r);

  const steps: Step[] = [
    {
      title: "Given",
      body: (
        <FormulaBlock>
          a = {fmtNum(a1)}, r = {fmtNum(r)}, n = {n}
        </FormulaBlock>
      ),
    },
    {
      title: "Formula — geometric sequence",
      body: (
        <FormulaWithLegend
          formula={<>aₙ = a · rⁿ⁻¹; &nbsp; Sₙ = a · (1 − rⁿ) / (1 − r), r ≠ 1</>}
          legend={[
            { sym: "a", def: "first term" },
            { sym: "r", def: "common ratio" },
            { sym: "n", def: "term index" },
          ]}
        />
      ),
    },
    {
      title: "Substitute — nth term",
      body: (
        <FormulaBlock>
          aₙ = {fmtNum(a1)} · ({fmtNum(r)})^({n} − 1) = {fmtNum(nth)}
        </FormulaBlock>
      ),
    },
    {
      title: "Substitute — sum",
      body:
        Math.abs(r - 1) < 1e-15 ? (
          <FormulaBlock>
            r = 1 → Sₙ = n·a = {n} · {fmtNum(a1)} = {fmtNum(sum)}
          </FormulaBlock>
        ) : (
          <FormulaBlock>
            Sₙ = {fmtNum(a1)} · (1 − {fmtNum(Math.pow(r, n))}) / {fmtNum(1 - r)} = {fmtNum(sum)}
          </FormulaBlock>
        ),
    },
    {
      title: "Answer",
      body: <FormulaBlock>aₙ = {fmtNum(nth)}, &nbsp; Sₙ = {fmtNum(sum)}</FormulaBlock>,
    },
  ];

  return { a1, r, n, terms, nth, sum, steps };
}

