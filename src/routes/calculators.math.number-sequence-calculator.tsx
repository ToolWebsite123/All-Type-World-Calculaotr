// ---------------- Number formatting ----------------

function fmtNum(n: number): string {
  if (Object.is(n, -0)) n = 0;
  if (!Number.isFinite(n)) return n > 0 ? "∞" : "−∞";
  const r = Math.round(n * 1e10) / 1e10;
  return r.toString();
}

/** Truncate a bigint's decimal string for display when it is very long. */
function fmtBig(n: bigint, maxDigits = 40): string {
  const s = n.toString();
  if (s.length <= maxDigits) return s;
  return `${s.slice(0, maxDigits - 6)}…${s.slice(-3)}  (${s.length} digits)`;
}

// ---------------- Line plot (shared) ----------------

interface Point {
  index: number;
  value: number;
}

/**
 * Small SVG line + scatter plot of the first several terms.
 * Auto-scales the y axis so linear, exponential and Fibonacci growth
 * are all visible on the same component. Falls back to numbers on the
 * axis; when values dwarf each other (geometric / Fibonacci) the
 * caller can pass logY.
 */
function SequencePlot({
  points,
  label,
  logY = false,
}: {
  points: Point[];
  label: string;
  logY?: boolean;
}) {
  if (points.length < 2) return null;

  const W = 560;
  const H = 220;
  const padL = 48;
  const padR = 16;
  const padT = 14;
  const padB = 34;
  const plotW = W - padL - padR;
  const plotH = H - padT - padB;

  // Map values (optionally through log10 with an offset for non-positive inputs).
  const rawVals = points.map((p) => p.value);
  const useLog = logY && rawVals.every((v) => v > 0);
  const mapY = (v: number) =>
    useLog ? Math.log10(Math.max(v, Number.MIN_VALUE)) : v;

  const yVals = rawVals.map(mapY);
  const yMin = Math.min(...yVals);
  const yMax = Math.max(...yVals);
  const ySpan = yMax - yMin || 1;
  const yPad = ySpan * 0.08;
  const yLo = yMin - yPad;
  const yHi = yMax + yPad;

  const xMin = points[0].index;
  const xMax = points[points.length - 1].index;

  const xToPx = (i: number) =>
    padL + ((i - xMin) / Math.max(1, xMax - xMin)) * plotW;
  const yToPx = (v: number) =>
    padT + plotH - ((mapY(v) - yLo) / (yHi - yLo)) * plotH;

  const zeroY =
    !useLog && yLo < 0 && yHi > 0 ? yToPx(0) : null;

  const linePts = points
    .map((p) => `${xToPx(p.index).toFixed(1)},${yToPx(p.value).toFixed(1)}`)
    .join(" ");

  // Choose x-axis tick spacing so labels don't crowd.
  const xTickStep = Math.max(1, Math.ceil((xMax - xMin) / 8));
  const xTicks: number[] = [];
  for (let i = xMin; i <= xMax; i += xTickStep) xTicks.push(i);
  if (xTicks[xTicks.length - 1] !== xMax) xTicks.push(xMax);

  // Four y ticks across the range.
  const yTicks: number[] = [];
  for (let k = 0; k <= 4; k++) {
    const mappedTick = yLo + ((yHi - yLo) * k) / 4;
    const value = useLog ? Math.pow(10, mappedTick) : mappedTick;
    yTicks.push(value);
  }

  const formatYTick = (v: number) => {
    const abs = Math.abs(v);
    if (abs !== 0 && (abs < 0.01 || abs >= 1e6)) return v.toExponential(1);
    if (Number.isInteger(v)) return v.toString();
    return v.toPrecision(3);
  };

  return (
    <div className="mt-4 rounded-lg border border-border bg-card/50 p-3">
      <div className="mb-2 flex items-center justify-between text-xs text-muted-foreground">
        <span className="font-medium">{label}</span>
        {useLog && <span className="italic">y axis: log₁₀ scale</span>}
      </div>
      <svg
        viewBox={`0 0 ${W} ${H}`}
        className="h-auto w-full"
        role="img"
        aria-label={label}
      >
        {/* y grid + labels */}
        {yTicks.map((v, i) => {
          const y = padT + plotH - (i * plotH) / 4;
          return (
            <g key={`y${i}`}>
              <line
                x1={padL}
                x2={W - padR}
                y1={y}
                y2={y}
                stroke="currentColor"
                strokeOpacity={0.1}
              />
              <text
                x={padL - 6}
                y={y + 4}
                textAnchor="end"
                className="fill-muted-foreground"
                fontSize="10"
              >
                {formatYTick(v)}
              </text>
            </g>
          );
        })}

        {/* axes */}
        <line
          x1={padL}
          y1={padT}
          x2={padL}
          y2={H - padB}
          stroke="currentColor"
          strokeOpacity={0.5}
        />
        <line
          x1={padL}
          y1={H - padB}
          x2={W - padR}
          y2={H - padB}
          stroke="currentColor"
          strokeOpacity={0.5}
        />

        {/* zero line when values straddle zero */}
        {zeroY !== null && (
          <line
            x1={padL}
            x2={W - padR}
            y1={zeroY}
            y2={zeroY}
            stroke="currentColor"
            strokeOpacity={0.35}
            strokeDasharray="3 3"
          />
        )}

        {/* x ticks */}
        {xTicks.map((t) => (
          <g key={`x${t}`}>
            <line
              x1={xToPx(t)}
              x2={xToPx(t)}
              y1={H - padB}
              y2={H - padB + 4}
              stroke="currentColor"
              strokeOpacity={0.5}
            />
            <text
              x={xToPx(t)}
              y={H - padB + 16}
              textAnchor="middle"
              className="fill-muted-foreground"
              fontSize="10"
            >
              {t}
            </text>
          </g>
        ))}

        {/* the line itself */}
        <polyline
          points={linePts}
          fill="none"
          stroke="var(--color-primary)"
          strokeWidth={2}
        />

        {/* term dots */}
        {points.map((p) => (
          <circle
            key={p.index}
            cx={xToPx(p.index)}
            cy={yToPx(p.value)}
            r={3.5}
            fill="var(--color-primary)"
          />
        ))}

        {/* axis labels */}
        <text
          x={padL + plotW / 2}
          y={H - 4}
          textAnchor="middle"
          className="fill-muted-foreground"
          fontSize="10"
        >
          term index n
        </text>
        <text
          x={14}
          y={padT + plotH / 2}
          textAnchor="middle"
          className="fill-muted-foreground"
          fontSize="10"
          transform={`rotate(-90 14 ${padT + plotH / 2})`}
        >
          value aₙ
        </text>
      </svg>
    </div>
  );
}

