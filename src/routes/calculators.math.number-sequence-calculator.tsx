interface ArithmeticResult {
  a1: number;
  d: number;
  n: number;
  terms: number[];
  nth: number;
  sum: number;
  steps: Step[];
}

function computeArithmetic(
  a1: number,
  d: number,
  n: number,
): ArithmeticResult {
  const terms: number[] = [];
  const listMax = Math.min(n, 20);
  for (let i = 1; i <= listMax; i++) terms.push(a1 + (i - 1) * d);
  const nth = a1 + (n - 1) * d;
  const sum = (n / 2) * (2 * a1 + (n - 1) * d);

  const steps: Step[] = [
    {
      title: "Given",
      body: (
        <FormulaBlock>
          a₁ = {fmtNum(a1)}, d = {fmtNum(d)}, n = {n}
        </FormulaBlock>
      ),
    },
    {
      title: "Formula — arithmetic sequence",
      body: (
        <FormulaWithLegend
          formula={<>aₙ = a₁ + (n − 1)·d; &nbsp; Sₙ = n/2 · (2a₁ + (n − 1)·d)</>}
          legend={[
            { sym: "a₁", def: "first term" },
            { sym: "d", def: "common difference" },
            { sym: "n", def: "term index" },
          ]}
        />
      ),
    },
    {
      title: "Substitute — nth term",
      body: (
        <FormulaBlock>
          aₙ = {fmtNum(a1)} + ({n} − 1)·{fmtNum(d)} = {fmtNum(nth)}
        </FormulaBlock>
      ),
    },
    {
      title: "Substitute — sum",
      body: (
        <FormulaBlock>
          Sₙ = {n}/2 · (2·{fmtNum(a1)} + ({n} − 1)·{fmtNum(d)}) = {fmtNum(sum)}
        </FormulaBlock>
      ),
    },
    {
      title: "Answer",
      body: <FormulaBlock>aₙ = {fmtNum(nth)}, &nbsp; Sₙ = {fmtNum(sum)}</FormulaBlock>,
    },
  ];

  return { a1, d, n, terms, nth, sum, steps };
}

